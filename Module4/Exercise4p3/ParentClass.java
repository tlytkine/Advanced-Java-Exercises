public class ParentClass {

	static int x1 = 1;
	private static int x2 = 1;
	public static int x3 = 1;
	protected static int x4 = 1;
	public static int x5 = 1;

	public static void main(String[]args) {
		System.out.println("Parent Class: ");
		System.out.println("no keyword int x1: " + x1);
		System.out.println("private int x2: " + x2);
		System.out.println("public int x3: " + x3);
		System.out.println("protected int x4: " + x4);
		System.out.println("public int x5: " + x5);	
	}

}
